            {
                 Login g=(Login)session.get(Login.class,  Login1.st);
        g.setAddress(str2);
        session.update(g);
           tx.commit();
            }
            if(str1.equalsIgnoreCase("City"))
            {
                 Login g=(Login)session.get(Login.class,  Login1.st);
        g.setCity(str2);
        session.update(g);
           tx.commit();
            }
            if(str1.equalsIgnoreCase("Pin"))
            {
                 Login g=(Login)session.get(Login.class,  Login1.st);
        g.setPin(str2);
        session.update(g);
           tx.commit();
            }
            JOptionPane.showMessageDialog(this, str1+" updated");
